            <h3>Narrative</h3>
            <p>This project was focused on Project Management and teamwork. We were put in random groups and were given a choice of topics we could do. We chose a restaurant project with the name, JEEN. This name was created from the initials of our 4 group members, Josel, Eunsol, Emma, and Neal. To stay organized, each week we would have agile scrum meetings to assign and discuss what we would do that week.</p>
