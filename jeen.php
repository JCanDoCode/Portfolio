        <article>
            <h3>Brainstorming</h3>
            <p>In this phase we created the user stories and brand identity. Our brand identity was to appeal to middle-high class clientele. This clientele would be people looking to eat somewhere fancy without breaking the bank. To achieve this demographic, we used gold on navy as our color scheme. We also chose photos that complement our color scheme. In the end, we created 20 user stories, a mood board, logo, inspiration links, and written content that would be on the website. User stories were helpful in deciding who and why someone uses the website.</p>
        </article>
        <div class="jeen-artifact"></div>
        <article>
            <h3>Design</h3>
            <p>Then, we designed the low fidelity wireframes for the website. Figma was a great app to use with a team since we could edit in real time with others. I was tasked with creating the menu page. One of the problems I faced here was to create a luxurious feel to the website. Something that occurred in most real-world examples were the abstract ways of conveying messages through elements. I solved this problem by adding sleek lines under the price to create this concept.</p>
        </article>
        <div class="jeen-artifact"></div>
